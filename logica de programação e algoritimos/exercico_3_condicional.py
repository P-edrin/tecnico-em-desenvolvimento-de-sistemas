dia = int(input("escreva o dia: " ))

if(dia == 1):
    print("domingo")

elif(dia == 2):
    print("segunda-feira")

elif(dia == 3):
    print("terça-feira")

elif(dia == 4):
    print("quarta-feira")

elif(dia == 5):
    print("quinta_feira")

elif(dia == 6):
    print("sexta")

elif(dia == 7):
    print("sabado")
    
else:
    print(" dia invalido!")